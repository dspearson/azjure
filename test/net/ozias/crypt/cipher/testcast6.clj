;; ### CAST6 Tests

(deftest ^{:doc "Test CAST6 spec test vectors"} testSpec
  (testing "Spec"
    (is (= true (every? true? (map encrypt-block c6spec-test-vectors))))
    (is (= true (every? true? (map decrypt-block c6spec-test-vectors))))))

(deftest ^{:doc "Test CAST6 block suites"} testBlock
  (testing "Block"
    (is (= true (every? true? (map encryptor c6block-test-vectors))))
    (is (= true (every? true? (map decryptor c6block-test-vectors))))))

(deftest ^{:doc "Test CAST6 stream suites"} testStream
  (testing "Stream"
    (is (= true (every? true? (map encryptor c6s-test-vectors))))
    (is (= true (every? true? (map decryptor c6s-test-vectors))))))

(deftest ^{:doc "Test CAST6 counter mode suite"} testCounter
  (testing "Counter"
    (is (= true (every? true? (map #(encryptor % :iv iv-64b) c6ctr-test-vectors))))
    (is (= true (every? true? (map #(decryptor % :iv iv-64b) c6ctr-test-vectors))))))

(deftest ^{:doc "Test CAST6"} testCAST6
  (testing "CAST6"
    (testSpec)
    (testBlock)
    (testStream)
    (testCounter)))

(defn ^{:doc "Namespace hook to run tests in proper order"} test-ns-hook
  []
  (testCAST6))
