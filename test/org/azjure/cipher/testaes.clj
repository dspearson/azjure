;; ### AES Tests

(deftest ^{:doc "Test AES spec test vectors"} testSpec
  (testing "Spec"
    (is (= true (every? true? (map encrypt-block aesspec-test-vectors))))
    (is (= true (every? true? (map decrypt-block aesspec-test-vectors))))))

(deftest ^{:doc "Test AES block suites"} testBlock
  (testing "Block"
    (is (= true (every? true? (map encryptor aesblock-test-vectors))))
    (is (= true (every? true? (map decryptor aesblock-test-vectors))))))

(deftest ^{:doc "Test AES stream suites"} testStream
  (testing "Stream"
    (is (= true (every? true? (map encryptor aess-test-vectors))))
    (is (= true (every? true? (map decryptor aess-test-vectors))))))

(deftest ^{:doc "Test AES counter mode suite"} testCounter
  (testing "Counter"
    (is (= true (every? true? (map #(encryptor % :iv iv-64b) aesctr-test-vectors))))
    (is (= true (every? true? (map #(decryptor % :iv iv-64b) aesctr-test-vectors))))))

(deftest ^{:doc "Test AES"} testAES
  (testing "AES"
    (testSpec)
    (testBlock)
    (testStream)
    (testCounter)))

(defn ^{:doc "Namespace hook to run tests in proper order"} test-ns-hook
  []
  (testAES))
