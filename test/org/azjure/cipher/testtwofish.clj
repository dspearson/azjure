;; ### Twofish Tests

(deftest ^{:doc "Test Twofish spec test vectors"} testSpec
  (testing "Spec"
    (is (= true (every? true? (map encrypt-block tfspec-test-vectors))))
    (is (= true (every? true? (map decrypt-block tfspec-test-vectors))))))

(deftest ^{:doc "Test Twofish block suites"} testBlock
  (testing "Block"
    (is (= true (every? true? (map encryptor tfblock-test-vectors))))
    (is (= true (every? true? (map decryptor tfblock-test-vectors))))))

(deftest ^{:doc "Test Twofish stream suites"} testStream
  (testing "Stream"
    (is (= true (every? true? (map encryptor tfs-test-vectors))))
    (is (= true (every? true? (map decryptor tfs-test-vectors))))))

(deftest ^{:doc "Test Twofish counter mode suite"} testCounter
  (testing "Counter"
    (is (= true (every? true? (map #(encryptor % :iv iv-64b) tfctr-test-vectors))))
    (is (= true (every? true? (map #(decryptor % :iv iv-64b) tfctr-test-vectors))))))

(deftest ^{:doc "Test Twofish"} testTwofish
  (testing "Twofish"
    (testSpec)
    (testBlock)
    (testStream)
    (testCounter)))

(defn ^{:doc "Namespace hook to run tests in proper order"} test-ns-hook
  []
  (testTwofish))
